1 - Anúncios particulares

São feitos por pessoas físicas, sem intermédio de empresas. Todos os itens anunciados deverão ser de uso pessoal, não tendo sido adquiridos ou produzidos com o objetivo principal de (re)venda.

 
2 - Anúncios profissionais

São considerados profissionais os anúncios feitos por pessoa jurídica ou pessoa física com atividades comerciais, devendo ser observadas as exigências impostas por lei.

A Nerdlândia reserva-se o direito de decidir quando um anúncio ou conta deve ser classificado como profissional.

Anunciantes que tenham simultaneamente mais do que 6 anúncios nas categorias de imóveis, veículos, barcos ou empregos, ou 100 anúncios no total, serão considerados profissionais.

Anúncios na categoria "Serviços" serão considerados profissionais. 

Na categoria Vagas de Emprego, particulares somente estão autorizados a anunciar vagas oferecidas diretamente por famílias, como: empregadas domésticas, cozinheiras, motoristas, babás, enfermeiras etc.

Na subcategoria "Lançamentos" será permitida somente a postagem de anúncios realizados por pessoas jurídicas que exerçam as atividades de incorporadoras, construtoras e/ou imobiliárias.

São feitos por pessoa jurídica ou pessoa física com atividades comerciais e devem observar as exigências impostas por lei.



3 - Conta de usuário

O usuário poderá criar apenas uma conta para uso na Nerdlândia, exceto para a contratação de Planos Profissionais.  

Será permitido indicar apenas um telefone e apenas um e-mail para a criação de uma conta na Nerdlândia;

A Nerdlândia poderá, a seu critério, efetuar a verificação dos números de telefone e/ou endereços de e-mail indicados. Ao cadastrar esses dados, a legitimidade e titularidade de tais informações está sob responsabilidade do usuário, estando ciente ainda que a Nerdlândia poderá excluir os anúncios e contas, ainda que as demais sejam do mesmo titular ou seja verificada a falsidade e/ou invalidade das informações fornecidas.

Não é permitida a utilização de e-mails, nomes e/ou nicknames que contenham as palavras “Nerdlândia”, “Desapega”, “Autoshift” e/ou “Storia”.

Não é permitida a utilização de nomes e/ou nicknames que contenham números de telefones, links, endereços, e-mails e/ou nº de CRECI.

 
4 - Multiplicidade de anúncios

A quantidade de anúncios publicados por cada anunciante é ilimitada, porém ressaltamos que a duplicação de anúncios de um mesmo produto/serviço é vedada.

Em algumas categorias e/ou subcategorias, poderá haver uma limitação da quantidade de anúncios gratuitos que podem ser inseridos na Categoria no mesmo mês. O usuário pode consultar as quantidades de anúncios gratuitos por categoria clicando aqui.

Ao ultrapassar a quantidade de anúncios inseridos no mesmo mês em cada categoria que se enquadrar na regra do item anterior, o anunciante que desejar publicar mais anúncios nas categorias em que tenha excedido o limite gratuito, poderá realizar a compra de uma ou mais unidades de anúncios. De acordo com a disponibilidade, também será possível contratar um Plano Profissional. Estas opções permitirão a inserção de um volume maior de anúncios na categoria contratada.

Uma vez postado o anúncio, este será contabilizado no limite de anúncios gratuitos ou contratados para a categoria e período contratados, ainda que ocorra a exclusão de um ou mais dos anúncios pelo usuário, seja por ação própria ou em caso de remoção por infração às regras do site.

Não é permitido:
I. Anunciar mais de um produto ou serviço no mesmo anúncio;
II. Postar mais de um anúncio do mesmo produto/serviço ou de produtos/serviços muito parecidos;
III. Inserir o mesmo anúncio em diferentes categorias, regiões ou cidades;
IV. Utilizar a mesma imagem em anúncios diferentes e,
V. Remover os anúncios e republicá-los com o propósito de colocá-los de volta ao topo dos resultados.
VI. Utilizar quaisquer dispositivos automatizados ou programas de computador que permitam a postagem de Anúncios e/ou Conteúdos no Site sem que cada um deles seja inserido manualmente pelo Usuário (dispositivo automatizado de postagem), inclusive, a título meramente exemplificativo, o uso de tais dispositivos para envio em massa de anúncios, ou o envio de anúncios em intervalos regulares. Excepcionalmente, é permitida a contratação de integradores pelos clientes que possuam Plano Profissional, sendo autorizada somente a contratação de integradores que tenham sido previamente homologados pela Nerdlândia. Para saber mais, clique aqui.

5 - Propagandas

Só é permitido realizar anúncios com a finalidade de ofertar concretamente um produto ou serviço. Não são permitidos anúncios que caracterizem mera propaganda.

6 - Serviços

Os serviços oferecidos ou desejados devem seguir os regulamentos e leis aplicáveis a cada profissão, inclusive regramentos de órgãos de classe que sejam cabíveis.


7 - Conteúdo do anúncio

O título e a descrição do anúncio devem descrever somente o produto e/ou serviço anunciado;

Não são permitidos anúncios e/ou imagens que:

I. Contenham palavras de busca que não estejam diretamente relacionadas ao item anunciado ou ainda que tenham o potencial de prejudicar a busca e localização de outros anúncios relacionados ao mesmo produto ou produtos semelhantes,
II. Contenham links, sites, nomes de empresas e/ou de pessoas ou caracteres desnecessários,
III. Utilizem motivos pessoais ou de caráter íntimo,
IV. Informem mais do que 03 (três) itens que poderiam formar a base de troca ou anúncios de troca de animais por itens materiais;
V. Caracterizem cópia de títulos ou descrição de outros anúncios;
VI. Possam ser percebidos como ofensivos e/ou discriminatórias a indivíduos, grupos étnicos, religiosos, figuras públicas, etc.

Permitimos apenas anúncios no idioma Português.

8 - Categorias

O anúncio deve ser inserido na categoria e subcategoria que melhor descreve o produto ou serviço;

 Produtos e serviços que não se encaixam na mesma categoria e subcategoria devem ser inseridos em anúncios separados;

Anúncios inseridos na categoria ou subcategoria incorreta poderão ser removidos da plataforma, a critério da Nerdlândia.

Saiba mais no link sobre as categorias aqui.

9 - Imagens

As imagens do anúncio devem ser relevantes ao produto ou serviço anunciado;

Na categoria “Vagas de emprego”, não é permitido inserir fotos dos candidatos a vagas de emprego ou de terceiros;

Não é permitido utilizar:

Logotipos de empresas (exceto nas categorias de empregos e serviços);
II. Imagens de outros anunciantes e empresas sem a autorização expressa dos mesmos;
III. Fotos de crianças, pessoas com traje de banho ou lingerie;
IV. Imagens que contenham links ou e-mail, exceto se o texto fizer parte do item da foto (ex.: modelos de cartões de visitas).


10 - Produtos e serviços não permitidos
Somente poderão ser veiculados na Nerdlândia anúncios de produtos e/ou serviços permitidos pelos Termos de Uso da Nerdlândia e pela lei vigente. Os anunciantes são responsáveis pelos produtos e serviços oferecidos e devem certificar-se da autenticidade e legalidade de seus anúncios. A Nerdlândia poderá cancelar anúncios que infrinjam os Termos de Uso de sua plataforma ou a Lei Vigente. Ao cadastrar um anúncio o usuário deve verificar a listagem exemplificativa de produtos e serviços não permitidos na Nerdlândia. Para visualizá-la, clique aqui.

11 - Ofertas irreais

Anúncios não realistas poderão ser removidos a exclusivo critério da Nerdlândia.
• O anunciante deverá preencher o campo "preço" com o valor total do produto ou deixá-lo em branco;
• Na hipótese do anunciante oferecer a possibilidade de pagamento parcelado, o valor das parcelas deverá ser indicado na descrição do anúncio, não sendo permitido indicá-lo no campo valor ou no título do anúncio.
• Caso o anúncio seja referente a uma doação ou troca, não preencha o campo preço.
• Valores divergentes entre campo preço e conteúdo do anúncio poderão ser removidos a exclusivo critério da Nerdlândia.


12 - Localização do item/serviço

O produto ou serviço anunciado deve estar no Brasil e na região em que foi publicado o anúncio.

Lembre-se:

Você só pode criar um anúncio para um mesmo produto/ serviço;

Você não pode inserir o mesmo anúncio em várias regiões ou várias categorias diferentes;

Os anúncios devem oferecer produtos e serviços específicos. Não aceitamos anúncios de propaganda;

Anúncios que não estão de acordo com as Regras poderão ser removidos;
