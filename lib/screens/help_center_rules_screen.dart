Regras para anúncios
As regras abaixo foram criadas para ajudá-lo na publicação de anúncios de boa qualidade e com relevância para compradores e interessados. 
Lembramos que estas Regras são constantemente revisadas e podem sofrer alterações a exclusivo critério da Nerdlândia. Recomendamos que você sempre consulte esta página antes de postar um anúncio.
 
